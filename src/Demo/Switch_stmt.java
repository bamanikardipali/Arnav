package Demo;

public class Switch_stmt 
{
	public static void main(String[] args) 
	{
		switch(6)
		{
		case 1:System.out.println("current month is January");
				break;
				
		case 2:	System.out.println("current month is February");
				break;
		
		case 3:System.out.println("current month is March");
				break;
				
		case 4:System.out.println("current month is April");
				break;
				
		case 5:System.out.println("current month is May");
		       break;
		       
		case 6:System.out.println("current month is June");
	       		break;
		
		default :
			System.out.println("Invalid input");
		
		}
	}
}
